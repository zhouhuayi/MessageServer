package com.choose.Message.bean;

public class RoomUser {
    private Long id;

    private Long userid;

    private Integer roomuserlevel;

    private Integer roomid;

    private Integer state;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getUserid() {
        return userid;
    }

    public void setUserid(Long userid) {
        this.userid = userid;
    }

    public Integer getRoomuserlevel() {
        return roomuserlevel;
    }

    public void setRoomuserlevel(Integer roomuserlevel) {
        this.roomuserlevel = roomuserlevel;
    }

    public Integer getRoomid() {
        return roomid;
    }

    public void setRoomid(Integer roomid) {
        this.roomid = roomid;
    }

    public Integer getState() {
        return state;
    }

    public void setState(Integer state) {
        this.state = state;
    }
}